from sklearn.datasets import load_breast_cancer
import pandas as pd
import numpy as np
#our goal is to train a classification model that will accurately predict breast cancer based on given parameters with a low amount of False Negatives
#understanding the dataset:

breast_cancer_data=load_breast_cancer()
print(breast_cancer_data.keys())
print(breast_cancer_data["DESCR"])
print(breast_cancer_data["target_names"])
print(breast_cancer_data["target"])
print(breast_cancer_data["feature_names"])

#framing the data

df=pd.DataFrame(breast_cancer_data["data"],columns=breast_cancer_data["feature_names"])

df["target"]=breast_cancer_data['target']

print(f"First 10 records:{df.head(10)}")
print(f"last 10 records:{df.tail(10)}")


#since the targets are binary discrete values(benign vs malignant) we will use a Logistic regression to predict breastcancer

from sklearn.model_selection import train_test_split


#training linear regression model on our data

X=df.drop("target",axis=1)
y=df["target"]

X_train,X_test,y_train,y_test=train_test_split(X,y,random_state=42,stratify=y,test_size=0.2)

from sklearn.linear_model import LogisticRegression
Logistic_Regression_Model=LogisticRegression(max_iter=500)

Logistic_Regression_Model.fit(X_train,y_train)

print("The Logistic regression model has been trained")

y_pred_log_reg= Logistic_Regression_Model.predict(X_test)

print("Predicted Values:", y_pred_log_reg[:10])
print("Real Values:",y_test.values[:10])

#comparison of test values with our predicted values by the trained classifiction model

from sklearn.metrics import accuracy_score,f1_score,confusion_matrix,classification_report

accuracy=accuracy_score(y_test,y_pred_log_reg)
print(f"the accuracy of this model is {accuracy}")
f1_score_=f1_score(y_test,y_pred_log_reg)
print("the precision of our model is",f1_score_)
cr=classification_report(y_test,y_pred_log_reg)

print("classification report:",cr)

#we are testing the accuracy of our model
# our model seems to have a high accuracy at 96% of prediction accuracy leaning closer to overfitting than underfitting

#we illustrate our confusion matrix

# on top of the high accuracy we can see that the outcome indicates FP>FN which is desired in cancer diagnostics


import seaborn as sns
import matplotlib.pyplot as plt

conf_matrix=confusion_matrix(y_test,y_pred_log_reg)
print(conf_matrix)
plt.figure(figsize=(6,6))
sns.heatmap(conf_matrix,annot=True,cmap="viridis",xticklabels=["Tested Negative","Tested Positive"],yticklabels=["Predicted Negative","Predicted Positive"])
plt.legend()
plt.title("Confusion matrix of Breast Cancer data prediction model")
plt.show()

#we train a decision tree model for our breast cancer data

from sklearn.tree import DecisionTreeClassifier
dt_model=DecisionTreeClassifier(random_state=42)
dt_model.fit(X_train,y_train)
y_pred_dt=dt_model.predict(X_test)
accuracy_dt=accuracy_score(y_test,y_pred_dt)
print("accuracy of our decision tree classifier",accuracy_dt)

import matplotlib.pyplot as plt

from sklearn.tree import plot_tree

plt.figure(figsize=(10,10))
plot_tree(dt_model,filled=True,feature_names=X.columns,class_names=["Malignant","Benign"])
plt.title("Decision tree Visualization")
plt.show()


#in conclusion: we have successfully trained a logistic regression model, with high accuracy, to avoid overfitting we should test and train it on more complex datasets